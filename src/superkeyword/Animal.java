package superkeyword;

public class Animal {

	String color = "green"; //instance variable

	public Animal() {  // default constructor
		System.out.println("from animal default const");
	}

	public Animal(String ster) {   // param constructor
		System.out.println("from animal param const");
	}

	public void m1() { // instance method
		System.out.println("from animal m1");
	}

}
